package g_method.b_exercise.b;

public class ProgrammingByFarzadAfi {
    public static void main(String[] args) {
        multipleWithTwo(5);
    }

    public static void multipleWithTwo(int number) {
        for (int i = 1; i <= number; i++) {
            System.out.print(i + " * 2 =" + i * 2 + "   /**/");
        }
    }
}
